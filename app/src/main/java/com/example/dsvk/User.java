package com.example.dsvk;

public class User {
    public String name, email, age, Phone;
    public void User(){}

    public User(String user_name, String user_age, String user_email, String user_Phone) {
        this.name = user_name;
        this.age = user_age;
        this.email = user_email;
        this.Phone = user_Phone;
    }

}
